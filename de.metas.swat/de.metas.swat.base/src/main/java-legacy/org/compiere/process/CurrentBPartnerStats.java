import java.util.logging.Level;

import org.compiere.util.DB;

/**
 * Update/ Insert BPartner Statistics
 * 
 * @author Mark Ostermann BPartner Stats are precalculated and updated/ inserted
 *         in Table c_bpartner_stats (for better DB Performance)
 */
public class CurrentBPartnerStats extends SvrProcess {

	/**
	 * Prepare - e.g., get Parameters.
	 */
	protected void prepare() {
		ProcessInfoParameter[] para = getParameter();
		for (int i = 0; i < para.length; i++) {
			String name = para[i].getParameterName();
			if (para[i].getParameter() == null) {
			} else {
				log.log(Level.SEVERE, "prepare - Unknown Parameter: " + name);
			}
		}
	} // prepare

	/**
	 * Perform process.
	 * 
	 * @return Message (clear text)
	 * @throws Exception
	 *             if not successful
	 */
	protected String doIt() throws Exception {
		log.info("CurrentBPartnerStats");

		String sql_upd = "SELECT update_bpstats();";
		String sql_ins = "SELECT insert_bpstats();";
		//
		DB.executeUpdate(sql_upd, get_TrxName());
		DB.executeUpdate(sql_ins, get_TrxName());
		return "@Success@ - ";
	} // doIt

} // NoteDelete
